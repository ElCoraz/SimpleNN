/**********************************************************************************************************************/
class Layer {
    /******************************************************************************************************************/
    int size; /* Размер */
    /******************************************************************************************************************/
    double[] biases; /* Смещение */
    double[] neurons; /* Нейрон */
    /******************************************************************************************************************/
    double[][] weights; /* Вес */

    /******************************************************************************************************************/
    Layer(int size, int nextSize) {
        this.size = size;

        biases = new double[size];
        neurons = new double[size];

        weights = new double[size][nextSize];
    }
}
